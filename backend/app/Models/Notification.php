<?php

namespace App\Models;

use Illuminate\Database\Eloquent\Factories\HasFactory;
use Illuminate\Database\Eloquent\Model;

class Notification extends Model
{
    use HasFactory;

    protected $table = 'notifications';

    protected $fillable = [
        'user_id',
        'type',
        'title',
        'message',
        'data',
        'is_read',
        'read_at',
    ];

    protected $casts = [
        'data' => 'array',
        'is_read' => 'boolean',
        'read_at' => 'datetime',
    ];

    // Laravel automaticky pridÃ¡ created_at a updated_at
    public $timestamps = true;

    // Notification types
    const TYPE_INTERNSHIP_CREATED = 'internship_created';
    const TYPE_INTERNSHIP_STATUS_CHANGED = 'internship_status_changed';
    const TYPE_COMMENT_ADDED = 'comment_added';
    const TYPE_DOCUMENT_UPLOADED = 'document_uploaded';
    const TYPE_APPROVAL_REQUEST = 'approval_request';

    /**
     * Notification belongs to a user
     */
    public function user()
    {
        return $this->belongsTo(User::class);
    }

    /**
     * Mark notification as read
     */
    public function markAsRead()
    {
        $this->update([
            'is_read' => true,
            'read_at' => now(),
        ]);
    }

    /**
     * Scope for unread notifications
     */
    public function scopeUnread($query)
    {
        return $query->where('is_read', false);
    }

    /**
     * Scope for read notifications
     */
    public function scopeRead($query)
    {
        return $query->where('is_read', true);
    }
}

